export function getQuestionsForLevel(
  mode: GameMode,
  difficulty: Difficulty,
  level: number,
): Question[] {
  const pool = ALL_QUESTIONS.filter((q) => q.mode === mode && q.difficulty === difficulty);
  if (pool.length === 0) return ALL_QUESTIONS.slice(0, QUESTIONS_PER_LEVEL);

  const start = ((level - 1) * QUESTIONS_PER_LEVEL) % pool.length;
  const result: Question[] = [];
  for (let i = 0; i < QUESTIONS_PER_LEVEL; i++) {
    result.push(pool[(start + i) % pool.length]);
  }
  return result;
}

export function getQuestionByIndex(
  mode: GameMode,
  difficulty: Difficulty,
  level: number,
  questionIndex: number,
): Question {
  const questions = getQuestionsForLevel(mode, difficulty, level);
  return questions[Math.min(questionIndex, questions.length - 1)] ?? questions[0]!;
}
